package basicprograms;

public class HandleVariable {

	public static void main(String[] args)
	{
		
		//E.g.		
		
		int num1 = 1000000000;
		long num2 = 1000000000000000000l;
		float num3 = 100000000000000000000000000000000000000.5f;
		double num4 = 100000000000000000000000000000000000000.5d;
		char grade = 'A';
		String name = "John";
		boolean write = true;
		
		System.out.println("My Integer Value = " + num1);	
		System.out.println("My Long Value = " + num2);
		System.out.println("My Float Value = " + num3);
		System.out.println("My Double Value = " + num4);
		System.out.println("My Character Value = " + grade);
		System.out.println("My String Value = " + name);
		System.out.println("My Boolean Value = " + write);
		
		
		//E.g.
		int num5;				//Variable Declaration
		num5 = 3;				//Variable Initialization
		System.out.println("num 5 = "+ num5);
		
		//E.g.
		int num6 = 1;
		num6 = 2;
		System.out.println("num 6 = "+ num6);
		
		//E.g.
		int num7 = 4, num8 = 5, num9 = 6;
		System.out.println("My num value 7= " + num7);
		System.out.println("My num value 8= " + num8);
		System.out.println("My num value 9= " + num9);
		
		
		
		int x = 10; int y = 20; int z = 30;
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		
		
		
		int a = 10; char ch = 'a'; String greeting = "Hi";
		System.out.println(a);
		System.out.println(ch);
		System.out.println(greeting);
		

	}
}
